package com.Abhishek;
import java.util.Scanner;
public class For_loop {
    public static void main(String[] args) {
        /*
        for loop Syntax:-
                for(initialization; conditions; increment/decrement)
                {
                        //body
                    }
         */
        // Print Number 1 to 5

        for(int i = 1; i <= 5; i++){
            System.out.println(+i);
        }
    }
}
